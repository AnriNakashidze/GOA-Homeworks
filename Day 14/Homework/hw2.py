# Homework: Make 5 examples of For Loop

# 1) This loop prints numbers from 0 to 4.
for i in range(5):
    print(i)

# 2) This loop prints each letter in the word "hello".
for letter in "hello":
    print(letter)

# 3) This loop prints numbers from 1 to 10.
for i in range(1, 11):
    print(i)

# 4) This loop prints the squares of numbers from 1 to 5.
for i in range(1, 6):
    print(i * i)

# 5) This loop prints numbers from 10 to 1 in reverse.
for i in range(10, 0, -1):
    print(i)
